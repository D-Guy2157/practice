# ARITHMETIC OPERATORS
# + Add
# - Subtract
# * Multiply
# / Divide(float)
# // Divide(int)
# % Remainder
# ** Exponent

x = 10
x = x + 3  # Incremental
x += 3  # Incremental Compact
print(x)

# OPERATOR PRECEDENCE
x = (10 + 3) * 2
print(x)

# COMPARISON OPERATORS
# > Greater than
# >= Greater than or Equal to
# < Less than
# <= Less than or equal to
# == Equal
# != Not Equal

x = 3 > 2  # Boolean Expression
print(x)

# LOGICAL OPERATORS
# and (both)
# or (at least one)
# not (inverse)

price = 25
print(price > 10 and price < 30)  # Between 10 and 30
print(not price > 10)  # Not greater than 10
