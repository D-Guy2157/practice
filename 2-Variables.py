# variable = a container for a value. Behaves as the value that it contains

# String:
first_name = "Dylan"
last_name = "Harvey"
full_name = first_name + " " + last_name

# print(type(first_name))  - Prints <class 'str'>
print("Hello " + full_name)

# Integer:
age = 17
# age += 1  # shortcut instead of age = age + 1
print("Your age is: " + str(age))
# print(type(age))  - Prints <class 'int'>

# Float:
height = 75.5
print("Your height is: " + str(height) + "in")
# print(type(height))  - Prints <class 'float'>

# Boolean:
human = True
print("Is human: " + str(human))
# print(type(human))  - Prints <class 'bool'>
