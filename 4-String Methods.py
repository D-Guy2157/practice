name = "Dylan Harvey"
#       0123456789

print(len(name))  # Prints the length of the string
print(name.find("d"))  # Finds the first occurrence of a character; prints index
print(name.capitalize())  # Capitalizes first letter in a string
print(name.upper())  # Capitalizes everything
print(name.lower())  # Makes the string lowercase
print(name.isdigit())  # Boolean output, checks if string is a number
print(name.isalpha())  # Boolean output, checks if string only contains alphabetical letters
print(name.count("y"))  # Prints the number of occurrences of a character
print(name.replace("y", "a"))  # Replaces all occurrences of one character with a second character
print(name * 3)  # Not technically a method but prints the string multiple times
