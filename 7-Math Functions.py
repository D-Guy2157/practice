import math

pi = 3.14
x = 1
y = 2
z = 3

print(round(pi))  # rounds to the nearest whole number
print(math.ceil(pi))  # rounds up to the nearest whole number
print(math.floor(pi))  # rounds down to the nearest whole number
print(abs(pi))  # absolute value
print(pow(pi,2))  # raises to a power of _
print(math.sqrt(pi))  # takes the square root, can't use negative
print(max(x,y,z))  # finds the max value
print(min(x,y,z))  # finds the min value
